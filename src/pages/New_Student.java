    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String query = "insert into studentinfo(studentid,first_name,last_name,branch,year,semester) values(?,?,?,?,?,?)";
        try {
            p = con.prepareStatement(query);
            p.setString(1, jTextField1.getText());
            p.setString(2, jTextField2.getText());
            p.setString(3, jTextField3.getText());
            p.setString(4, (String)jComboBox1.getSelectedItem());
            p.setString(5, (String)jComboBox2.getSelectedItem());
            p.setString(6,(String)jComboBox3.getSelectedItem());
            p.execute();
            JOptionPane.showMessageDialog(null,"Student Added Suceessfully");
        } catch (Exception e) {
        e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

